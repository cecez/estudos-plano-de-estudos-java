public class TestaPontoFlutuante {

    public static void main(String[] args) {

        double salario;
        salario = 6403.10;
        System.out.println("Meu salário é de " + salario);

        double salario2 = 12806.20;
        System.out.println("Meu salário será de " + salario2);

        int divisaoInteira = 5 / 2;
        System.out.println(divisaoInteira);

        // Java avalia que é uma divisão entre inteiros e resultado será inteiro.
        double divisaoInteiraComDouble = 5 / 2;
        System.out.println(divisaoInteiraComDouble);

        double divisaoDouble = 5.0 / 2;
        System.out.println(divisaoDouble);


    }

}
