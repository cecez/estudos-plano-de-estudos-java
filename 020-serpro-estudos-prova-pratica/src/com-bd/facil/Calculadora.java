/*
Exercício: Crie uma classe Calculadora que tenha métodos para adição, subtração, multiplicação e divisão de dois números inteiros. Escreva testes unitários para verificar se os métodos da Calculadora estão funcionando corretamente.
*/

public class Calculadora {

	public static int adicionar(int a, int b) {
		return a+b;
	}

	public static int subtrair(int a, int b) {
		return a-b;
	}

	public static int dividir(int a, int b) {
		return a/b;
	}
	
}